//--------------------------------------------------------------
#include "../../Util/Counter/Counter.h"
//--------------------------------------------------------------


//--------------------------------------------------------------
Counter::Counter( const int& initVal ): val( initVal )
{
}
//--------------------------------------------------------------


//--------------------------------------------------------------
int Counter::getCountVal() const
{
	return val;
}
//--------------------------------------------------------------


//--------------------------------------------------------------
void Counter::decrement()
{
    val--;
}
//--------------------------------------------------------------


//--------------------------------------------------------------
void Counter::increment()
{
    val++;
}
//--------------------------------------------------------------


//--------------------------------------------------------------
void Counter::reset( const int& resVal )
{
    val = resVal;
}
//--------------------------------------------------------------
