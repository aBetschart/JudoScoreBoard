#include "Time.h"

Time::Time( const int& pMin, const int& pSec ):
sec( pSec % 60 ), min( pMin )
{

}
//--------------------------------------------------------------


//--------------------------------------------------------------
void Time::decSec()
{
    if( !isZero() )
    {
        if( getSec() == 0 )
        {
            sec = 59;
            decMin();
        }
        else
        {
            sec--;
        }
    }
}
//--------------------------------------------------------------


//--------------------------------------------------------------
void Time::incSec()
{
    if( getSec() == 59 )
    {
        sec = 0;
        incMin();
    }
    else
    {
        sec++;
    }
}
//--------------------------------------------------------------


//--------------------------------------------------------------
void Time::decMin()
{
    if( getMin() != 0 )
        min--;
}
//--------------------------------------------------------------


//--------------------------------------------------------------
void Time::incMin()
{
    min++;
}
//--------------------------------------------------------------


//--------------------------------------------------------------
int Time::getSec() const
{
    return sec;
}
//--------------------------------------------------------------


//--------------------------------------------------------------
int Time::getLowerSec() const
{
    return (sec % 10);
}
//--------------------------------------------------------------


//--------------------------------------------------------------
int Time::getUpperSec() const
{
    return (sec / 10);
}
//--------------------------------------------------------------


//--------------------------------------------------------------
int Time::getMin() const
{
    return min;
}
//--------------------------------------------------------------


//--------------------------------------------------------------
bool Time::isZero() const
{
    return( (getMin() == 0) && (getSec() == 0) );
}
//--------------------------------------------------------------


//--------------------------------------------------------------
void Time::reset(const int& pSec, const int& pMin)
{
    sec = (pSec % 60);
    min = pMin;
}
//--------------------------------------------------------------
